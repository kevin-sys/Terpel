 CREATE OR REPLACE PROCEDURE DELETE_CLIENTE(
  C_CEDULA IN CLIENTE.Cedula%TYPE,
   C_RESULT OUT VARCHAR2
 )
 AS
 BEGIN
   DELETE FROM CLIENTE
   WHERE Cedula = C_CEDULA;
  C_RESULT:= 'SE ELIMINÃ“ CORRECTAMENTE EL CLIENTE';
  COMMIT;
  EXCEPTION
    WHEN OTHERS THEN
          C_RESULT:= 'ERROR AL ELIMINAR'; 
        ROLLBACK;  
 END;
 
 
