Cedula VARCHAR2(11) PRIMARY KEY,
PrimerNombre VARCHAR2(25),
SegundoNombre VARCHAR2(25),
PrimerApellido VARCHAR2(25),
SegundoApellido VARCHAR2(25),
Telefono VARCHAR2(10),
Email VARCHAR2(50),
