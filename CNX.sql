  CREATE OR REPLACE PROCEDURE SELECT_CLIENTE_ID(
  C_CEDULA IN CLIENTE.Cedula%TYPE,
 C_CURSOR OUT  SYS_REFCURSOR
 )
 AS
 BEGIN
   OPEN C_CURSOR FOR
   SELECT CEDULA ,
PRIMERNOMBRE ,
SEGUNDONOMBRE ,
PRIMERAPELLIDO ,
SEGUNDOAPELLIDO ,
TELEFONO ,
EMAIL ,
DIRECCION ,
EDAD ,
CIUDAD ,
COMUNA ,
BARRIO ,
NACIONALIDAD     
    FROM CLIENTE WHERE Cedula = C_CEDULA;
 
   END;

